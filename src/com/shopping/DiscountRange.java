package com.shopping;

public class DiscountRange {

	  int price;
	  int discount;
	  
	  
	public DiscountRange(int price, int discount) {
		super();
		this.price = price;
		this.discount = discount;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getDiscount() {
		return discount;
	}
	public void setDiscount(int discount) {
		this.discount = discount;
	}
	  
	  
}
